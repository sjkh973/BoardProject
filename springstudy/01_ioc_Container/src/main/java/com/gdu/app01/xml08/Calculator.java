package com.gdu.app01.xml08;

public class Calculator {

	
	public double add(double height, double weight) {
		return height + weight;
	}
	public double sub(double height, double weight) {
		return height - weight;
	}
	public double mul(double height, double weight) {
		return height * weight;
	}
	public double div(double height, double weight) {
		return height / weight;
	}
	
	
	
}
